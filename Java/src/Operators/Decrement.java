package Operators;

public class Decrement {
      public static void main(String[] args)
      {
    	  int i=21;
    	  System.out.println(i);//21
    	  System.out.println(--i);//20
    	  System.out.println(i);//20
    	  //PostDecrement first Assign->Decrement
    	  System.out.println(i--);//20
    	  System.out.println(i);//19
    	  
      }
}
