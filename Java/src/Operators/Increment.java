package Operators;

public class Increment {
     public static void main(String[] args)
     {
    	 int i = 20;  
    	 System.out.println(i);//20
    	 System.out.println(++i);//21
    	 System.out.println(i);
    	 //postIncrement Assign->Increment
    	 System.out.println(i++);//21
    	 System.out.println(i);//22
    	 
    	
    	 }  
    	 
     }

